    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        if("".equals(inputNomorrmh.getText())||"".equals(inputNama.getText())||"".equals(inputNomor.getText())||"".equals(inputVilla.getSelectedItem()))
        {
            JOptionPane.showMessageDialog(this, "Data Invalid","Error",JOptionPane.WARNING_MESSAGE);
        }
        else{
            try {
                st = con.createStatement();
                st.executeUpdate("INSERT INTO bukutelpon VALUES('" + inputNomorrmh.getText() + "','"
                    + inputNama.getText() + "','"
                    + inputNomor.getText() + "','"
                    + inputVilla.getSelectedItem() + "')");
                kosongkanTabel();
                kosongkan();
                tampilkanDiTabel();
                JOptionPane.showMessageDialog(null, "Kontak berhasil disimpan");
            } catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Kontak tidak berhasil disimpan");
          }
        }
        
        
       

    }//GEN-LAST:event_simpanActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        try {
            con.createStatement().executeUpdate("UPDATE bukutelpon SET kode_rmh ='"+inputNomorrmh.getText()+"',nama ='"+inputNama.getText()+"',nmr_tlp ='"+inputNomor.getText()+"',villa ='"+inputVilla.getSelectedItem()+"' WHERE kode_rmh ='"+inputNomorrmh.getText()+"'");
            kosongkanTabel();
            kosongkan();
            tampilkanDiTabel();
            simpan.setEnabled(true);
            JOptionPane.showMessageDialog(null, "Kontak berhasil diupdate");
        } catch (SQLException ex) {
            Logger.getLogger(formBuku.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Kontak tidak berhasil diupdate");
            simpan.setEnabled(true);
        }
    }//GEN-LAST:event_updateActionPerformed

    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
        try {
            PreparedStatement ps = con.prepareStatement("DELETE FROM bukutelpon WHERE kode_rmh = ?");
            String nomorNya = inputNomorrmh.getText();
            ps.setString(1, nomorNya);
            ps.executeUpdate();
            simpan.setEnabled(true);
            JOptionPane.showMessageDialog(null, "Kontak berhasil dihapus");
        } catch (SQLException ex) {
            java.util.logging.Logger.getLogger(formBuku.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Kontak tidak berhasil dihapus");
            simpan.setEnabled(true);
         }finally{
            try {
                kosongkan();
                kosongkanTabel();
                tampilkanDiTabel();
            } catch (SQLException ex) {
                java.util.logging.Logger.getLogger(formBuku.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_hapusActionPerformed

