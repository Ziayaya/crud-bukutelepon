    public ArrayList[] getTlpList() throws SQLException{      
        String queryCount = "SELECT COUNT(*) as c FROM bukutelpon";
              
        st = con.createStatement();        
        rsCount = st.executeQuery(queryCount);
        int sizeTable=0;

        while(rsCount.next()){
            sizeTable = rsCount.getInt("c");
        }       
        ArrayList[] tlpList = new ArrayList[sizeTable];       
        String query = "SELECT * FROM bukutelpon";
           rs = st.executeQuery(query);

       int x=0;
        while(rs.next()){
            tlpList[x] = new ArrayList<>();
            tlpList[x].add(rs.getString("kode_rmh"));
            tlpList[x].add(rs.getString("nama"));
            tlpList[x].add(rs.getString("nmr_tlp"));
            tlpList[x].add(rs.getString("villa"));
            x++;
        }
        return tlpList;
    }
    
    // menampilkan database di form
    public void tampilkanDiTabel() throws SQLException{
        ArrayList[] list = getTlpList();
        DefaultTableModel model = (DefaultTableModel)tabel.getModel();
       
        Object[] row = new Object[9];
        for(int i = 0; i<list.length; i++){
            row[0] = i+1;
            row[1] = list[i].get(0);
            row[2] = list[i].get(1);
            row[3] = list[i].get(2);
            row[4] = list[i].get(3);

            model.addRow(row);
        }       
    }
    
